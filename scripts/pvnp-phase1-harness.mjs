#!/usr/bin/env node
// scripts/pvnp-phase1-harness.mjs
//
// End-to-end orchestrator for the SUNDOG_V_P_V_NP Phase 1 toy verifier
// v0 run. Runs each stage in dependency order; bails on first failure.
//
// Stages:
//   1. generate environments + manifest
//   2. policies roster
//   3. train small_mlp via behavior cloning (Python)
//   4. baselines + calibration + traces
//   5. signatures
//   6. verifier decisions
//   7. ablations
//   8. attackers (inversion via Python, spoof in JS)
//   9. cost rollup
//  10. privilege-leak audit
//  11. falsifier summary
//  12. README.md inside run dir

import { spawnSync } from "node:child_process";
import { mkdir, writeFile } from "node:fs/promises";
import { performance } from "node:perf_hooks";
import path from "node:path";
import { fileURLToPath } from "node:url";

import { getPhase1RunConfig } from "./lib/pvnp-phase1-run-config.mjs";

const REPO_ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");

const DEFAULT_RUN_DIR = "results/pvnp/phase1-toy-verifier-v0";

function parseArgs(argv) {
  const args = { runDir: DEFAULT_RUN_DIR, skipMlpTrain: false };
  for (let i = 0; i < argv.length; i += 1) {
    if (argv[i] === "--run-dir") { args.runDir = argv[i + 1]; i += 1; }
    else if (argv[i] === "--skip-mlp-train") { args.skipMlpTrain = true; }
    else throw new Error(`Unknown flag: ${argv[i]}`);
  }
  return args;
}

function runNode(script, args, label) {
  const t0 = performance.now();
  console.log(`\n[harness] ${label}`);
  const res = spawnSync(process.execPath, [path.join(REPO_ROOT, script), ...args], {
    cwd: REPO_ROOT, stdio: "inherit",
  });
  if (res.status !== 0) {
    console.error(`[harness] ${label} FAILED (exit ${res.status})`);
    process.exit(res.status ?? 1);
  }
  const elapsed = (performance.now() - t0) / 1000;
  console.log(`[harness] ${label} done in ${elapsed.toFixed(2)}s`);
}

function runPython(module, args, label) {
  const t0 = performance.now();
  console.log(`\n[harness] ${label}`);
  const res = spawnSync("python", ["-m", module, ...args], {
    cwd: REPO_ROOT, stdio: "inherit",
  });
  if (res.status !== 0) {
    console.error(`[harness] ${label} FAILED (exit ${res.status})`);
    process.exit(res.status ?? 1);
  }
  const elapsed = (performance.now() - t0) / 1000;
  console.log(`[harness] ${label} done in ${elapsed.toFixed(2)}s`);
}

async function writeRunReadme(runDir) {
  const slate = getPhase1RunConfig(runDir);
  const sourceBound = slate.schema_suffix === "v1" || slate.schema_suffix === "v2";
  const isV2 = slate.schema_suffix === "v2";
  const md = [
    `# Phase 1 ${slate.schema_suffix} Run Artifacts`,
    ``,
    `Run id: \`${slate.run_id}\`.`,
    ``,
    `Generated by \`scripts/pvnp-phase1-harness.mjs\`.`,
    ``,
    `## Artifact map`,
    ``,
    `| File | Producer | Purpose |`,
    `| --- | --- | --- |`,
    `| \`manifest.json\` | generate-environments | ${slate.schema_suffix} slate + commit + counts + env hash |`,
    `| \`environments.jsonl\` | generate-environments | 832 envs (with hidden state for evaluator) |`,
    `| \`policies.jsonl\` | policies | policy roster (hc_safe, hc_decoy, small_mlp) |`,
    `| \`mlp_policy_small_mlp_seed_0.json\` | training/pvnp/train_mlp_policy.py | BC-trained MLP weights (JS-loadable) |`,
    `| \`traces.jsonl\` | baselines | per-(policy, env) trajectory (positions + probes + actions) |`,
    `| \`calibration_manifest.json\` | baselines | m_min sweep + selected value + insulation proof |`,
    `| \`ground_truth_labels.csv\` | baselines | evaluator-only Safe(pi, env) labels |`,
    `| \`baseline_decisions.csv\` | baselines | rollout + full-state + formal decisions |`,
    ...(sourceBound ? [
      `| \`trace_commitments.jsonl\` | signatures | source-bound trace commitments for integrity checks |`,
    ] : []),
    `| \`signatures.jsonl\` | signatures | sigma certificates |`,
    ...(sourceBound ? [
      `| \`integrity_decisions.csv\` | verifier | normal-certificate integrity check records |`,
      `| \`integrity_failures.csv\` | verifier | synthetic integrity-mismatch probes |`,
    ] : []),
    `| \`verifier_decisions.csv\` | verifier | V decisions on measurement splits |`,
    `| \`ablation_decisions.csv\` | ablations | vacuity-probe decisions per dropped field |`,
    ...(isV2 ? [
      `| \`geometry_boundary_audit.csv\` | verifier | v2 geometry promise flags by measurement pair |`,
      `| \`accepted_oop_audit.csv\` | verifier | v2 accepted out-of-promise rows by subtype |`,
    ] : []),
    `| \`attacker_inversion_results.json\` | training/pvnp/train_inversion_attacker.py | A_inv_small per-env AUROC/IoU |`,
    `| \`attacker_trials.csv\` | attackers | combined inversion + spoof per-trial outcomes |`,
    `| \`costs.csv\` | costs | wall_ms + ops per component, derived ratios |`,
    `| \`audit-report.json\` / \`audit-report.txt\` | privilege-audit | static grep audit for forbidden tokens |`,
    `| \`falsifier_summary.md\` | falsifier-summary | named falsifier disposition + headline numbers |`,
    ``,
    `## Reproduce`,
    ``,
    `\`\`\`bash`,
    slate.schema_suffix === "v0" ? `npm run pvnp:phase1` : `npm run pvnp:phase1:${slate.schema_suffix}`,
    `\`\`\``,
    ``,
    `or run the harness directly:`,
    ``,
    `\`\`\`bash`,
    `node scripts/pvnp-phase1-harness.mjs --run-dir ${path.relative(REPO_ROOT, runDir).replace(/\\/g, "/")}`,
    `\`\`\``,
    ``,
    `See \`${slate.slate_path}\` for the frozen slate.`,
    slate.receipt_path
      ? `See \`${slate.receipt_path}\` for the reviewed receipt.`
      : `No reviewed receipt has been filed for this slate yet.`,
    ``,
  ].join("\n");
  await writeFile(path.join(runDir, "README.md"), md, "utf8");
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const outDir = path.resolve(REPO_ROOT, args.runDir);
  await mkdir(outDir, { recursive: true });

  const stageArgs = ["--run-dir", args.runDir];
  const harnessStart = performance.now();

  runNode("scripts/pvnp-phase1-generate-environments.mjs", ["--out", args.runDir], "stage 1: generate environments");
  runNode("scripts/pvnp-phase1-policies.mjs", stageArgs, "stage 2: policy roster");
  runNode("scripts/pvnp-phase1-baselines.mjs", stageArgs, "stage 3: baselines bootstrap (HC policies; MLP auto-skipped)");
  if (!args.skipMlpTrain) {
    runPython("training.pvnp.train_mlp_policy", ["--run-dir", args.runDir, "--epochs", "8"], "stage 4: BC train small_mlp");
    runNode("scripts/pvnp-phase1-baselines.mjs", stageArgs, "stage 5: baselines re-run (with MLP picked up)");
  }
  runNode("scripts/pvnp-phase1-signatures.mjs", stageArgs, "stage 6: signatures");
  runNode("scripts/pvnp-phase1-verifier.mjs", stageArgs, "stage 7: verifier decisions");
  runNode("scripts/pvnp-phase1-ablations.mjs", stageArgs, "stage 8: ablations (vacuity probes)");
  runNode("scripts/pvnp-phase1-attackers.mjs", stageArgs, "stage 9: attackers (inversion + spoof)");
  runNode("scripts/pvnp-phase1-costs.mjs", stageArgs, "stage 10: cost rollup");
  runNode("scripts/pvnp-phase1-privilege-audit.mjs", stageArgs, "stage 11: privilege-leak audit");
  runNode("scripts/pvnp-phase1-falsifier-summary.mjs", stageArgs, "stage 12: falsifier summary");

  await writeRunReadme(outDir);
  const totalMs = performance.now() - harnessStart;
  console.log(`\n[harness] done in ${(totalMs / 1000).toFixed(2)}s; artifacts in ${args.runDir}`);
}

main().catch((err) => { console.error(err); process.exit(1); });
